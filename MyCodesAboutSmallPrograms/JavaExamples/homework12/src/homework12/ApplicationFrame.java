    public static String printNumbers(Collection<Integer> numbers) {
        String result = "";
        Iterator<Integer> iterator = numbers.iterator();

        while (iterator.hasNext()) {
            result += String.format("%d ", iterator.next());
        }
        return result;
    }

    public static String removeDublicateNeightborsNumbers(List<Integer> numbers) {
        String result = "";
        ListIterator<Integer> iterator = numbers.listIterator();

        while (iterator.hasNext()) {
            if (iterator.previousIndex() == -1) {
                continue;
            }
            if (iterator.previous() == iterator.next()) {
                iterator.remove();
            }
            result = String.format("%d ", iterator.next());
        }
        return result;
    }

